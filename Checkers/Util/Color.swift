import UIKit

extension UIColor {
    static var backgroundColor: UIColor { return UIColor(hex: 0x212121) }
    static var highlightColor: UIColor { return UIColor(hex: 0xFF3D00) }

    static var boardDark: UIColor { return UIColor(hex: 0x009688) }
    static var boardLight: UIColor { return UIColor(hex: 0xE0E0E0) }

    static var buttonDisabled: UIColor { return UIColor(hex: 0x9e9e9e) }
}
